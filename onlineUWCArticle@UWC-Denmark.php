				</div>
				<div class="row">
					<div class="col-md-12">
					<br>
					<p class="lead"><b>UWC steps into the digital age</b></p>

						<p>
							Online UWC is an innovative supplement to the more traditional educational opportunities that UWC currently offers. As of June 2014, UWC was a movement consistent of physical schools and colleges, a series of spontaneous summer camps and a limited variety of additional outreach programmes for educators and students alike. UWCs mission is to united people, cultures and nations through education, and through this unification create a sustainable future. With this mission in mind, the company OurCommonFuture has tapped into the potential of the internet and online interaction in creating the course ‘OnlineUWC’.
						</p>
						<p>
							OurCommonFuture was founded by Polish Hanna Celina (UWC-USA ’03), British Robin Tyne (UWC Adriatic ’12) and Danish Sahra-Josephine Hjorth (UWC-USA ’04) in February 2014. The course is built on a vision to reach a much wider audience than the traditional UWC schools and colleges are currently able to reach: “In a digital age, it is essential that we use all interactive tools and means of communication made available to us, also social media, live-streaming and blogs, in maximizing exposure to the UWC mission. Moreover, Facebook, twitter, YouTube, blogs and other fantastic tools available on web 2.0 allow us to reach a wider range of students with different profiles, interests and skills. We need to meet the youth on their terms” says co-founder Sahra-Josephine Hjorth. The first online class ran for five weeks starting late June 2014. The first class worked as a pilot, with the objective of actively testing the online format and testing whether it is suitable for the creation of an interactive forum, in which students gain new knowledge and experiences, are exposed to an international student body, make friends from across the globe, and gain new skills and competences making the students able to act as agents of positive change in their local environments, whether that be Russia, Italy or Denmark.
						</p>
						<p>
							The theme of the course is <i>sustainability</i>, and the term sustainability should be understood in the widest sense of the word; students are encouraged to explore multiple aspects of sustainability, such as environmental or climate issues, human migration, architecture and design, or in terms of peace and conflict resolution. The course is founded on a belief that online learning and virtual interactions can foster change offline in the participants local communities. Co-founder Sahra-Josephine Hjorth stresses that the students who took part in the pilot class are inspired to also meet in the physical world when possible, that they exchange information about their educational plans, and are preparing to set up local initiatives, inspired by in talented entrepreneurs and speakers they met during the five-week programme.
						</p>
						<p>
							Today, opportunities for online education and virtual exchange of knowledge are endless. But does this necessarily mean that UWC should take part in the revolution of education and embrace the online format? We have asked co-founder of Online UWC Sahra-Josephine Hjorth five important questions about the Online UWC program and how it measures up to the more traditional educational offers UWC has been delivering for decades.
						</p>
						<br><br>
						<p class="lead"><b>5 quick questions for Online UWC co-founder Sahra-Josephine Hjorth</b></p>
						<br>
						<p class="lead"><b>Can you get a UWC experience online?</b></p>
						<p>
							That is a really important question, and I am happy you are asking it. I am a big fan of a quote by Samuel Johnson, in which he argues that “almost all absurdity of conduct arises from the imitation of those whom we cannot resemble.” It is essential to me that Online UWC does not become an imitation of UWC is its current form,  but that we maintain that we are a new and alternative initiative. For some, Online UWC is a supplement to the traditional two-year UWC education, while for others, it is a way to prepare themselves for a two-year stay at a college, or become better acquainted with UWC, in order to decided whether or not they are interested in applying for a school in the future. At the same time, some of the students are not planning to apply to UWC at all.
						</p>
						<p>
							One of the reason why we founded Online UWC is to actively challenge and change the fact that only a microscopic amount of people in the world are actually familiar with UWC. This makes UWC an unnecessarily exclusive and exclusionary universe. I am personally active in the selection of students for the two-year stay at a UWC school in both Denmark and Russia. While it is joyous to help students be accepted to a UWC school, the selection process is, for me, also characterized by a sense of despair. UWC annually rejects a very high amount of insanely enthusiastic and talented young people, because the UWC schools can only take a limited amount of students. Moreover, many are too old when they apply, because they have not heard about UWC at the right age, and others are rejected because they are not suited for the IB program. Online UWC provides these talented people with an opportunity and a forum to interact and engage with the UWC mission, other passionate students, a group of enthusiastic UWC alumni who volunteer with the program, and experts in sustainability.
						</p>
						<br>
						<p class="lead"><b>Why should UWC provide online education?</b></p>
						<p>
							Because the online medium enables us to teach in alternative ways, and because the IB is terribly one-dimensional. Exactly because we are providers in online education, we have the opportunity to allow the students to solve different problems and assignments than we would ask them to solve the in the regular classroom. To understand my claim, I’m going to invite you into our virtual classroom and introduce you to the way we work. Now, the program spans over five weeks, and each week the students vote on what dimension of sustainability they want to learn about the upcoming week.  Each Sunday, the weekly topic is released. The topic can be related to city planning, sustainable human migration or something completely different from those two topics. Mondays we release the materials the students are expected to be familiar with that week. Since we believe in collaborative learning, we typically only provide the students with a small amount of work to prepare. This is usually a new article and a TED talk. After the materials have been released, we ask the students to identify further information related to the topic we are working with themselves. And the students then share the best videos and articles with one another on their class Facebook page. In this way, students are responsible for their own education, and they ‘show up’ for the live sessions better equipped to discuss the topic from different perspectives, based on diversified preparation.
						</p>
						<p>
							We meet every Wednesday, where we use google Hangout to interact with a guest lecturer. The lecturer typically prepared a lecture which lasts 15-30 min., after which the students can ask the lecturer questions and discuss the topic with one another and the class mentors. It is our experience that the students also want to take responsibility for inviting the guest lecturers they find most exciting. Isn’t that amazing? And needless to say that we encourage that. On Thursdays, we meet for an hour, during which the students work in smaller groups.
						</p>
						<p>
							People often ask me if students are assigned homework, which they are. But at Online UWC we do not expect them to deliver something in a specific predefined format, and it does not have to be in writing. Some students prefer to write an assignment, others make Powerpoint presentations. Students report back in different ways, others may go out and take pictures of their city and write a commentary for it or make a short film. We have created a space where there is room for that, as long as the students share their work and experiences with the rest of the class, so that we may learn from one another.
						</p>
						<br>
						<p class="lead"><b>Do participants need a specific profile in order to be eligible to apply?</b></p>
						<p>
							No they do not. They simply have to be between 14 and 20 years old and have internet access. We are currently pursuing solutions so that we may include people in the program who do not have internet access or a computer in their home, for example through sponsoring computers to schools.
						</p>
						<br>
						<p class="lead"><b>What is your goal in terms of participants in Denmark and globally?</b></p>
						<p>
							Our next class starts September 29th 2014, and we expect to have 100 students for that program. Over the span of the next five years, we plan to reach 10.000 students annually. But that will only be possible with help from more amazing UWC alumni!
						</p>
						<br>
						<p class="lead"><b>How can alumni get involved?</b></p>
						<p>
							That’s simple.  You can sign up to be a mentor on our website at www.onlineuwc.org or via the Facebook page at www.facebook.com/onlineuwc.
							Mentors only sign up to volunteer three hours per week over a five-week period, so it’s something most people can actually manage, regardless of whether they are students or working professionals.
						</p>
