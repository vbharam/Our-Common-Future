					<h3>OurCommonFuture and the Online UWC course have been featured in an article published by UWC Denmark</h3>
					<span class="meta bottomspace30">By <a href="#" title="Posts by Roy Sanders" rel="author">Sahra Josephine</a><span class="bullet">•</span> September 1, 2014 <span class="bullet">•</span><a href="#" title="View all posts in Food &amp; Health" rel="category tag"> #onlineUWC  #Sustainability</a></span>
				</div>
				<div class="row">
					<div class="col-md-12">
						<p class="lead">
							 On Friday August 29th 2014, OurCommonFuture and the Online UWC course on sustainability were featured in an article published by UWC Denmark. The original version of the article is in Danish and can be read <span> <a href="http://dk.uwc.org/blog" target="_blank">here</a></span>.
						</p>
						<div class="text-center">
							<img src="img/demo/onlineUWCPic.jpg" class="img-responsive aligncenter" alt="">
						</div>
